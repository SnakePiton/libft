int	ft_isalpha(int c)
{
	if ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A'))
		return (1);
	return (0);
}

int	ft_isalnum(int c)
{
	if ((ft_isalpha(c) == 1) || (c <= '9' && c >= '0'))
		return (1);
	return (0);
}
